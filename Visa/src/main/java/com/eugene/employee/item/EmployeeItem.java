package com.eugene.employee.item;

public class EmployeeItem {
	private int empId;
	private String empName;
	private String empNation;
	private String empPhone;
	private String empNumber;
	private String empVisa;
	private String empDate;
	private String empMemo;
	private int companyId;
	
	public int getEmpId() {
		return empId;
	}
	public void setEmpId(int empId) {
		this.empId = empId;
	}
	public String getEmpName() {
		return empName;
	}
	public void setEmpName(String empName) {
		this.empName = empName;
	}
	public String getEmpNation() {
		return empNation;
	}
	public void setEmpNation(String empNation) {
		this.empNation = empNation;
	}
	public String getEmpPhone() {
		return empPhone;
	}
	public void setEmpPhone(String empPhone) {
		this.empPhone = empPhone;
	}
	public String getEmpNumber() {
		return empNumber;
	}
	public void setEmpNumber(String empNumber) {
		this.empNumber = empNumber;
	}
	public String getEmpVisa() {
		return empVisa;
	}
	public void setEmpVisa(String empVisa) {
		this.empVisa = empVisa;
	}
	public String getEmpDate() {
		return empDate;
	}
	public void setEmpDate(String empDate) {
		this.empDate = empDate;
	}
	public String getEmpMemo() {
		return empMemo;
	}
	public void setEmpMemo(String empMemo) {
		this.empMemo = empMemo;
	}
	public int getCompanyId() {
		return companyId;
	}
	public void setCompanyId(int companyId) {
		this.companyId = companyId;
	}
}

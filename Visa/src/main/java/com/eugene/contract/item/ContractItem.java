package com.eugene.contract.item;

public class ContractItem {
	private String companyId;
	private String employeeId;
	private String contractDate;
	private String contractRevenue;
	private String contractPayment;
	private String contractInsurance;
	private String contractDepositeDate;
	private String contractBalance;
	private String contractMemo;
	
	public String getCompanyId() {
		return companyId;
	}
	public void setCompanyId(String companyId) {
		this.companyId = companyId;
	}
	public String getEmployeeId() {
		return employeeId;
	}
	public void setEmployeeId(String employeeId) {
		this.employeeId = employeeId;
	}
	public String getContractDate() {
		return contractDate;
	}
	public void setContractDate(String contractDate) {
		this.contractDate = contractDate;
	}
	public String getContractRevenue() {
		return contractRevenue;
	}
	public void setContractRevenue(String contractRevenue) {
		this.contractRevenue = contractRevenue;
	}
	public String getContractPayment() {
		return contractPayment;
	}
	public void setContractPayment(String contractPayment) {
		this.contractPayment = contractPayment;
	}
	public String getContractInsurance() {
		return contractInsurance;
	}
	public void setContractInsurance(String contractInsurance) {
		this.contractInsurance = contractInsurance;
	}
	public String getContractDepositeDate() {
		return contractDepositeDate;
	}
	public void setContractDepositeDate(String contractDepositeDate) {
		this.contractDepositeDate = contractDepositeDate;
	}
	public String getContractBalance() {
		return contractBalance;
	}
	public void setContractBalance(String contractBalance) {
		this.contractBalance = contractBalance;
	}
	public String getContractMemo() {
		return contractMemo;
	}
	public void setContractMemo(String contractMemo) {
		this.contractMemo = contractMemo;
	}
}
